package CursoJavaNelioAlves4;

public class Java1 {
    public static void main(String[] args) {

        /* Variáveis que começam com número, inserir um _ na frente */
        int _5minutos;

        /* Não utilize acentos ou til */
        int salario;

        /* Não utilize espaço, utilize maiúsculas na primeira letra da palavra (Menos a primeira).
        Utilize também nomes que façam sentido */
        int salarioDoFuncionario;

        /* Para a primeira letra de uma variável, inicie com minúscula.
         Para Classes, inicie a palavra com uma letra maiúscula */

    }
}
