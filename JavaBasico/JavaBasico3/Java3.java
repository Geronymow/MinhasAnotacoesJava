package CursoJavaNelioAlves3;

public class Java3 {
    public static void main(String[] args) {

       int x = 0;
       int y = 4;

       while (x < 3) {
           // Enquanto x > 3 o programa roda, printando os valores constantes de x e y.
           y += 2;
           x += 1;
           System.out.println(x + " - " + y);
       }

    }
}
