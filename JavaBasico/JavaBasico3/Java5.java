package CursoJavaNelioAlves3;

public class Java5 {
    public static void main(String[] args) {


        for (int x = 4; x >= 0; x--){
            System.out.println("Valor de x: "+x);
            /* Contagem regressiva */
        }

        System.out.println();

        int y = 5;
        for (System.out.println("Testando a contagem!"); y > 0 ; y--) {
            System.out.println("Valor de y: "+y);
        }
    }
}
