package CursoJavaNelioAlves1;

public class Java2 {
    public static void main(String[] args) {

        String nome = "Maria";
        int idade = 31;
        double renda = 3200.67;
        System.out.printf("%s tem %d anos e possui uma " +
                        "renda total de %.2f reais %n",
                nome, idade, renda);

/*
%s Puxa a variável String
%d Puxa a variável inteiro
%f Puxa a variável com casas decimais
%n Quebra de linha

Sempre escreva na sequência
*/

    }
}

